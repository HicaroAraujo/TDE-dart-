void main(){
    String cor = "black";
    switch(cor){
        case "blue":
            print("Azul");
            break;
        case "red":
            print("vermelho");
            break;
        case "green":
            print("verde");
            break;
        case "yellow":
            print("amarelo");
            break;
        default:
         print("Cor não reconhecida");
   
}

}
